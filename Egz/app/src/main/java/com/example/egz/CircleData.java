package com.example.egz;

import android.graphics.Color;

public class CircleData {
    public int color;
    public int radius;
    public int x;
    public int y;
    public int borderSize;


    public CircleData(int color, int radius, int x, int y, int borderSize) {
        this.color = color;
        this.radius = radius;
        this.x = x;
        this.y = y;
        this.borderSize = borderSize;
    }


}
